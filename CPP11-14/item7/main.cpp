// ============================================================
// Effective Modern C++ Item7: 区别使用()和{}创建对象
// ============================================================
// 核心结论：
//   大括号初始化更安全，但遇到 initializer_list 构造函数会优先匹配
//   圆括号和大括号在某些情况下结果完全不同
// ============================================================

#include <iostream>
#include <vector>
#include <initializer_list>

// ============================================================
// 【Part 1】大括号初始化的三个优点
// ============================================================

void part1_brace_advantages()
{
    // 优点1：禁止窄化转换，更安全
    double d = 3.14;
    // int a{d};   // 编译报错！不允许窄化转换
    int b(d);      // OK，但精度丢失，编译器只是警告
    int c = d;     // OK，但精度丢失

    // 优点2：避免函数声明歧义
    // Widget w();   // 被解析为函数声明，不是创建对象！
    // Widget w{};   // 明确是创建对象，没有歧义
 
    // 优点3：容器元素直接初始化
    std::vector<int> v{1, 2, 3, 4, 5};  // 直接初始化5个元素
}

// ============================================================
// 【Part 2】最重要的坑：initializer_list 构造函数优先匹配
// ============================================================

class Widget {
public:
    Widget(int i, bool b)  { std::cout << "构造函数1: int, bool\n"; }
    Widget(int i, double d){ std::cout << "构造函数2: int, double\n"; }
    Widget(std::initializer_list<int> list) {
        std::cout << "构造函数3: initializer_list, size=" << list.size() << "\n";
    }
};

void part2_initializer_list_priority()
{
    Widget w1(10, true);   // 调用构造函数1（圆括号，正常匹配）
    Widget w2(10, 3.14);   // 调用构造函数2（圆括号，正常匹配）
 
    Widget w3{10, true};   // 调用构造函数3！true→1，变成{10,1}
    Widget w4{10, 3.14};   // 调用构造函数3！3.14→3，变成{10,3}
    // 大括号只要看到 initializer_list 构造函数，不惜一切去匹配它
}

// ============================================================
// 【Part 3】最典型的例子：vector 圆括号 vs 大括号
// ============================================================

void part3_vector_difference()
{
    std::vector<int> v1(10, 20);  // 10个元素，每个值为20
    std::vector<int> v2{10, 20};  // 2个元素，值为10和20
 
    std::cout << "v1.size() = " << v1.size() << "\n";  // 10
    std::cout << "v2.size() = " << v2.size() << "\n";  // 2
}

// ============================================================
// 【Part 4】空大括号是特例
// 空大括号 = 没有参数，调用默认构造函数
// 不是空的 initializer_list
// ============================================================

class Widget2 {
public:
    Widget2()                              { std::cout << "默认构造函数\n"; }
    Widget2(std::initializer_list<int> l)  { std::cout << "initializer_list构造\n"; }
};
 
void part4_empty_brace()
{
    Widget2 w1{};    // 调用默认构造函数，不是 initializer_list
    Widget2 w2({});  // 调用 initializer_list 构造，传入空列表
    Widget2 w3;      // 调用默认构造函数
}
 
// ============================================================
// 【核心总结】
// ============================================================
//
// 1. 大括号初始化的优点
//    禁止窄化转换（double→int 直接报错）
//    避免函数声明歧义（Widget w{} 不会被误解为函数声明）
//    可以直接初始化容器元素
//
// 2. 最重要的坑
//    有 initializer_list 构造函数时，大括号优先匹配它
//    即使其他构造函数更合适，也会强行匹配 initializer_list
//
// 3. vector 圆括号 vs 大括号
//    v(10, 20) → 10个值为20的元素
//    v{10, 20} → 2个元素，值为10和20
//    结果完全不同！
//
// 4. 空大括号
//    {} → 调用默认构造函数
//    ({}) → 调用 initializer_list 构造函数，传入空列表
//
// 5. 实际建议
//    选一种风格坚持用，不要混用
//    需要指定容器大小时用圆括号，避免歧义
//    普通变量初始化用大括号更安全
//
// ============================================================
 
int main()
{
    part1_brace_advantages();
    part2_initializer_list_priority();
    part3_vector_difference();
    part4_empty_brace();
    return 0;
}